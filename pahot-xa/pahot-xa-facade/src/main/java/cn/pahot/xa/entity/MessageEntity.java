package cn.pahot.xa.entity;

import java.io.Serializable;

/**
 * 消息实体类
 * @author hj87080234@gmail.com
 * @date 2016年8月29日
 */
public class MessageEntity implements Serializable{
	
	private static final long serialVersionUID = 3668791427338464020L;
	/**
	 * 消息编号
	 */
	private Long id;
	/**
	 * 消息内容
	 */
	private String content;
	/**
	 * 消息数据类型,1-json,2-xml
	 */
	private String type;
	/**
	 * 消息来源编号
	 */
	private String source;
	/**
	 * 消费队列名称
	 */
	private String queuename;
	/**
	 * 重试次数
	 */
	private int repeatcount;
	
	/**
	 * 是否死亡,01未死亡,01已死亡
	 */
	private String isdead;
	
	/**
	 * 状态,01待确认,02已确认,03发送中,04已完成
	 */
	private String state;
	
	/**
	 * 延迟发送时间
	 */
	private Long delaytime;
	
	/**
	 * 备注
	 */
	private String remark;
	
	/**
	 * 创建者
	 */
	private String aa01;
	/**
	 * 创建者姓名
	 */
	private String aa11;
	
	/**
	 * 创建时间
	 */
	private long aa02;
	
	/**
	 * 修改人
	 */
	private String ab01;
	/**
	 * 修改人姓名
	 */
	private String ab11;
	
	/**
	 * 修改时间
	 */
	private long ab02;
	
	
	/**
	 * 扩展字段1
	 */
	private String ext1;
	
	/**
	 * 扩展字段2
	 */
	private String ext2;
	
	/**
	 * 扩展字段3
	 */
	private String ext3;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getQueuename() {
		return queuename;
	}

	public void setQueuename(String queuename) {
		this.queuename = queuename;
	}

	public int getRepeatcount() {
		return repeatcount;
	}

	public void setRepeatcount(int repeatcount) {
		this.repeatcount = repeatcount;
	}

	public String getIsdead() {
		return isdead;
	}

	public void setIsdead(String isdead) {
		this.isdead = isdead;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getAa01() {
		return aa01;
	}

	public void setAa01(String aa01) {
		this.aa01 = aa01;
	}

	public String getAa11() {
		return aa11;
	}

	public void setAa11(String aa11) {
		this.aa11 = aa11;
	}

	public long getAa02() {
		return aa02;
	}

	public void setAa02(long aa02) {
		this.aa02 = aa02;
	}

	public String getAb01() {
		return ab01;
	}

	public void setAb01(String ab01) {
		this.ab01 = ab01;
	}

	public String getAb11() {
		return ab11;
	}

	public void setAb11(String ab11) {
		this.ab11 = ab11;
	}

	public long getAb02() {
		return ab02;
	}

	public void setAb02(long ab02) {
		this.ab02 = ab02;
	}

	public String getExt1() {
		return ext1;
	}

	public void setExt1(String ext1) {
		this.ext1 = ext1;
	}

	public String getExt2() {
		return ext2;
	}

	public void setExt2(String ext2) {
		this.ext2 = ext2;
	}

	public String getExt3() {
		return ext3;
	}

	public void setExt3(String ext3) {
		this.ext3 = ext3;
	}

	public Long getDelaytime() {
		return delaytime;
	}

	public void setDelaytime(Long delaytime) {
		this.delaytime = delaytime;
	}
	
}
