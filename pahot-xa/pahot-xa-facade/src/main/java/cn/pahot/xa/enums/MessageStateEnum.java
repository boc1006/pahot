package cn.pahot.xa.enums;

/**
 * 消息状态
 * @author hj87080234@gmail.com
 * @date 2016年9月6日
 */
public enum MessageStateEnum {
	CONFIRMING("01", "待确认"), 
	CONFIRMED("02", "已确认"), 
	SENDDING("03", "发送中"), 
	SENDED("04","已完成");
	
	// 成员变量
	public String key;
	public String value;

	// 构造方法
	private MessageStateEnum(String key, String value) {
		this.key = key;
		this.value = value;
	}
}
